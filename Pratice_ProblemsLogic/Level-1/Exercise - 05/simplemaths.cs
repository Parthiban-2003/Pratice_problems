using System;

namespace Pratice_ProblemLogic
{
    public static class SimpleMath
    {
        public static int Add(int num1, int num2)
        {
            return num1 + num2;
        }

        public static int Subtract(int val1, int val2)
        {
            return val1 - val2;
        }

        public static int Multiply(int val3, int val4)
        {
            return val3 * val4;
        }

        public static int Divide(int val5, int val6)
        {
            return val5 / val6;
        }
    }
}
